// EXENPLO DE TIPAGEM DINÂMICA
let dado = 100;
console.log("valor", dado);
console.log("tipo", typeof dado);

// mudar o tipo da mesma variavel ( o que define a tipagem dinâmica)
dado = "ola, sou um texto";
console.log("novo valor ", dado);
console.log("novo tipo", typeof dado);

// exenplo da diferença entre let e const
const cpf = "123.456.789-00";
console.log("cpf", cpf);
